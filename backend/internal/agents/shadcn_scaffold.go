package agents

func shadcnCoreDeps() []string {
	return []string{
		"clsx",
		"class-variance-authority",
		"tailwind-merge",
		"@radix-ui/react-slot",
		"@radix-ui/react-dialog",
	}
}

func shadcnPackageDeps() []string {
	return []string{
		"tailwindcss-animate",
	}
}

func applyDeterministicShadcnScaffold(createIfMissing func(path, content, language string)) {
	if createIfMissing == nil {
		return
	}

	createIfMissing("components.json", shadcnComponentsJSON(), "json")
	createIfMissing("src/lib/utils.ts", shadcnUtilsTS(), "typescript")
	createIfMissing("src/components/ui/button.tsx", shadcnButtonTSX(), "typescript")
	createIfMissing("src/components/ui/card.tsx", shadcnCardTSX(), "typescript")
	createIfMissing("src/components/ui/input.tsx", shadcnInputTSX(), "typescript")
	createIfMissing("src/components/ui/badge.tsx", shadcnBadgeTSX(), "typescript")
	createIfMissing("src/components/ui/dialog.tsx", shadcnDialogTSX(), "typescript")
}

func shadcnScaffoldContent(path string) (string, string, bool) {
	switch path {
	case "components.json":
		return shadcnComponentsJSON(), "json", true
	case "src/lib/utils.ts":
		return shadcnUtilsTS(), "typescript", true
	case "src/components/ui/button.tsx":
		return shadcnButtonTSX(), "typescript", true
	case "src/components/ui/card.tsx":
		return shadcnCardTSX(), "typescript", true
	case "src/components/ui/input.tsx":
		return shadcnInputTSX(), "typescript", true
	case "src/components/ui/badge.tsx":
		return shadcnBadgeTSX(), "typescript", true
	case "src/components/ui/dialog.tsx":
		return shadcnDialogTSX(), "typescript", true
	default:
		return "", "", false
	}
}

